                <b>Historia Toysworld España</b><br><br>

                Toysworld nace en 2020 en Manises, Valencia, cuando un grupo de alumnos, con tan solo 20-23 años, decide hacer de un sueño de infancia una realidad y convierte el pequeño establecimiento de un kebab del pueblo en una tienda de mobiliario de bebé.<br><br>

                Pasado algún tiempo, los alumnos comienzan a prestar atención a la cada vez más frecuente búsqueda por parte de sus clientes de juguetes para niños de más edad. Después de meses escuchando las más variadas solicitudes, deciden dar el paso y comienzan a vender juguetes. El primero que añade a su inventario es un gimnasio de cuna que pronto demuestra el enorme interés del consumidor en este tipo de mercado. El joven grupo añade a los artículos que ya tiene a la venta triciclos, libros y otros juguetes.<br><br>

                Así, desde la ilusión de los niños se levantan las bases de la marca más icónica de juguetes en todo el mundo.<br><br>

                Comienza así un modelo de negocio dedicado totalmente a los niños, en la época del baby boom. Desde entonces, la empresa Toysworld y su mascota, el cerdo Dubaldo, recorren un largo camino.<br><br>

                Con el desarrollo del negocio, en 2020 se lanza la marca BabiesWorld, la principal cadena del mundo especializada en bebés y dedicada exclusivamente a productos de puericultura. Con tiendas propias o integradas en las tiendas Toysworld.<br><br>

                Toysworld España es un proyecto sólido que actualmente, tras la adquisición, cuenta con un patrimonio líquido de 100 millones de euros. La adquisición tuvo como objetivo revitalizar la marca por vía de un nuevo modelo de gestión que busca una serie de innovaciones y cambios en tienda; la revitalización de los espacios y novedades como el centro de la experiencia para garantizar así la continuidad de la marca.<br><br>

                Toysworld en España cuenta actualmente con 59 tiendas y más de 1300 colaboradores y es hoy una empresa enfocada en la familia y en todas las generaciones. Con una operación multicanal, presenta innovaciones para que todos encuentren la alegría en su día a día.
